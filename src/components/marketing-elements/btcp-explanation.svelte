<div>
    <h1 class="font-lg">Summary:</h1>
    <span class="font-thin">
        ERC20 BTC tokens are much more scalable than the Bitcoin network, taking full advantage of the merits of the Ethereum network. BTCP allows you to access these benefits in the safest possible way. It’s a diversified portfolio including multiple different providers, minimizing negative exposure in the event one asset loses its peg.
    </span>

    <h1 class="mt-4">Objective:</h1>
    <div class="font-thin">
        Tokenized BTC allows users to gain exposure to cryptocurrency’s largest coin without having to interact with the Bitcoin network, which can often be congested and costly. Over $2.8b of BTC has migrated to the Ethereum network through a multitude of different providers. These service providers can be centralized or decentralized, with both carrying an element of risk and the possibility of losing their peg.
        <br/><br/>
        BTCP minimizes the damage caused by such an event by diversifying your BTC holdings between four different service providers. If any one token fell to 0%, holders of this pie would only lose just 25% instead of everything.
    </div>

    <h1 class="mt-4">Methodology:</h1>

    <div class="font-thin">
        BTCP contains four of the leading BTC tokens on the market.
        <br/><br/>
        Unlike our DeFi pies that are weight-based, as a community we voted to evenly diversify BTCP between the four selected tokens. That’s because this pie is focused on risk minimisation. A 25% allocation evenly distributes the risk and minimizes the potential for damage.
    </div>

    
    <h1 class="mt-4">Operation:</h1>

    <div class="font-thin">
        BTCP is a tokenized representation of the four underlying ERC20s. When a user mints BTCP, our contract either purchases the underlying tokens from decentralized markets, or uses available tokens in the users wallet. The funds are safely stored in our non-custodial treasury, meaning that every single BTCP token is fully backed by reserves. Users can buy BTCP on the open market, and redeem for the underlying tokens at any time.
        <br/><br/>
        The token is created using a Balancer pool, which constantly automatically rebalances to maintain the desired allocation. This is done at no cost to holders of BTCP.
    </div>
    
