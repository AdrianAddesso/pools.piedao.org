    <h1 class="mt-4">Methodology:</h1>

    <div class="font-thin">
        Allocation weight is determined by market cap, with larger assets receiving a greater share. This enables maximum future growth without bottlenecks, as otherwise smaller project’s lack of liquidity can limit the size a pie can grow to.
        <br/><br/>

        Our backtesting shows that the more diversified an index, the better it performs. Not only that wide diversification ensures minimal risk exposure, meaning DXP is the safest DeFi index too. 
            <br/><br/>

        Following the same process as all our indices, DXP’s allocation is the result of weeks of community research and discussion, ultimately being voted into existence. 
    </div>

    
    <h1 class="mt-4">Operation:</h1>

    <div class="font-thin">
        DXP is a tokenized representation of a basket of relevant assets, with the underlying tokens safely stored in our treasury. The index is constructed using Balancer smart pools, meaning that it constantly rebalances and reacts to market changes in real-time. There is no withdrawal fee, and a highly competitive 0.7% streaming fee rewards DOUGH holders for their governance of the system.
        <br/><br/>
        We also provide a DXP staking contract where users can provide liquidity in return for a steady stream of DOUGH.
