    <div class="font-thin">
        DLP is a tokenized representation of the seven biggest projects in DeFi. When a user mints DLP our contract purchases the underlying assets from Uniswap and other service providers, sending the tokens to our treasury. As such the DAO ensures that DLP tokens are fully backed, and can be redeemed at any time at the press of a button.
        <br/><br/>
        The index is built using Balancer smart pools, which constantly rebalance and react to market changes in real-time. There is no withdrawal fee, and a highly competitive 0.7% streaming fee rewards DOUGH holders for their governance of the system.
        <br/><br/>
        It’s also possible to stake DLP in our DLP / ETH pool. Users are able to provide liquidity in return for a steady stream of DOUGH. This pool enables an efficient market for other users to easily swap in and out of the DLP token.
